/** \class Metal metals.hpp
 *  \brief Physical definition of a metallic material
 *
 *  The metal class contains the experimental data of a metallic materials.
 *  These values can be used in a Conductor or ConductorReflection class in
 *  order to accurately reproduce the appearance of the material
 */
class Metal
{
public:

    /** \brief Default constructor
     *
     *  \param[in] ior A list of float containing the spectrum values
     *  (following the Spectrum implementation) representing the wavelength
     *  dependent ior
     *  \param[in] abs A list of float containing the spectrum values
     *  (followinf the Spectrum implementation) representing the wavelength
     *  dependent absorption
     */
    Metal(const std::initializer_list<float> ior,
          const std::initializer_list<float> abs):n(ior),k(abs){};

    ///The index of refraction of the metal
    const Spectrum n;
    ///The absorption coefficient of the metal
    const Spectrum k;
};

